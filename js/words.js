const WordManager = {
    usedIndices: new Set(),

    getRandomWord() {
        if (this.usedIndices.size >= WORD_BANK.length) {
            this.usedIndices.clear();
        }
        let idx;
        do {
            idx = Math.floor(Math.random() * WORD_BANK.length);
        } while (this.usedIndices.has(idx));
        this.usedIndices.add(idx);
        return WORD_BANK[idx];
    },

    reset() {
        this.usedIndices.clear();
    }
};
